# 1 .定义变量保存节日
holiday_name = "情人节"
# 2. 如果是情人节就买玫瑰看电影
if holiday_name == "情人节":
    print("买玫瑰")
    print("看电影")
# 如果是平安夜就买苹果吃大餐
elif holiday_name == "平安夜":
    print("买苹果")
    print("吃大餐")
# 如果是生日就买蛋糕吃大餐
elif holiday_name == "生日":
    print("买蛋糕")
    print("吃大餐")
# 如果啥都不是就打印每天都是节日
else:
    print("每天都是节日啊")
